package model;

public class CandidatoProfissao {
	
	private String cpf;
	private int idProf;

	public CandidatoProfissao() {
	}

	public String getCpf() {
		return cpf;
	}

	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

	public int getIdProf() {
		return idProf;
	}

	public void setIdProf(int idProf) {
		this.idProf = idProf;
	}
	
	

}
